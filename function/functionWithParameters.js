// create a greetTo function with one parameter
// parameter should take name as string
function greetTo(name) {
  console.log('Hello!!!!', name);
  console.log(`Hello!!!!, ${name}`);
}

// call greetTo function with one argument 
// argument should be name of a person
greetTo('Pervez');

// create a greetToWithMessage function with two parameter
// first parameter should take name as string
// second parameter should take message as string
function greetToWithMessage(name, massage){
  console.log(`${massage}, ${name}`)
}

// call greetToWithMessage function with two arguments
// first arguments should take name as string
// second arguments should take message as string
greetToWithMessage('Pervez', 'Good Morning');

